import React from 'react';

interface SearchResultProps {
  result: {
    filename: string;
    fileHash: string;
    size: number;
    mimeType: string;
    peerId: string;
    peerDisplayName: string;
    addedAt: number;
  };
  onDownload: () => void;
}

export function SearchResult({ result, onDownload }: SearchResultProps) {
  const fileCategory = getFileCategory(result.mimeType);
  const icon = getFileIcon(fileCategory);

  return (
    <div className="card card-hover p-4 slide-in">
      <div className="flex items-start gap-4">
        {/* File type icon */}
        <div className={`file-icon file-icon-${fileCategory}`}>
          {icon}
        </div>

        {/* File info */}
        <div className="flex-1 min-w-0">
          <h3 className="font-semibold text-white truncate" title={result.filename}>
            {result.filename}
          </h3>
          <div className="flex items-center gap-4 mt-1 text-sm text-dark-400">
            <span className="flex items-center gap-1">
              <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M16 7a4 4 0 11-8 0 4 4 0 018 0zM12 14a7 7 0 00-7 7h14a7 7 0 00-7-7z" />
              </svg>
              {result.peerDisplayName}
            </span>
            <span className="flex items-center gap-1">
              <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 7v10c0 2 1 3 3 3h10c2 0 3-1 3-3V7c0-2-1-3-3-3H7C5 4 4 5 4 7z" />
              </svg>
              {formatBytes(result.size)}
            </span>
            <span className="flex items-center gap-1">
              <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 8v4l3 3m6-3a9 9 0 11-18 0 9 9 0 0118 0z" />
              </svg>
              {formatTimeAgo(result.addedAt)}
            </span>
          </div>
        </div>

        {/* Actions */}
        <div className="flex items-center gap-2">
          <button
            onClick={onDownload}
            className="btn btn-primary flex items-center gap-2"
          >
            <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 16v1a3 3 0 003 3h10a3 3 0 003-3v-1m-4-4l-4 4m0 0l-4-4m4 4V4" />
            </svg>
            Download
          </button>
        </div>
      </div>

      {/* File hash (for verification) */}
      <div className="mt-3 pt-3 border-t border-dark-700">
        <p className="text-xs text-dark-500 font-mono truncate" title={result.fileHash}>
          SHA256: {result.fileHash}
        </p>
      </div>
    </div>
  );
}

function getFileCategory(mimeType: string): string {
  if (mimeType.startsWith('video/')) return 'video';
  if (mimeType.startsWith('audio/')) return 'audio';
  if (mimeType.startsWith('image/')) return 'image';
  if (mimeType.startsWith('text/') || mimeType.includes('document') || mimeType.includes('pdf')) return 'document';
  if (mimeType.includes('zip') || mimeType.includes('tar') || mimeType.includes('rar') || mimeType.includes('7z')) return 'archive';
  return 'other';
}

